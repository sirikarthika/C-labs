#include <stdio.h>

int main() {
    int a = 10;          
    float b = 12.34;     
    double c = 123.456; 
    char d = 'S';        

    // Display using format specifiers
    printf("Integer : %d\n", a);
    printf("Float : %f\n", b);
    printf("Double : %lf\n", c);
    printf("Character : %c\n", d);
}